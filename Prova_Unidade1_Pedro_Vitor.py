def receberHora():
    h = int(input('Horas: '))
    m = int(input('Minutos: '))
    return [h, m]

def converterPara12(hora):
    h = hora[0]
    m = hora[1]
    if h == 0:
        return f'{h}:{m} A'
    elif h == 12:
        return f'{h}:{m} P'
    elif h > 12:
        h -= 12
        return f'{h}:{m} P'
    else:
        return f'{h}:{m} A'
converterPara12(receberHora())

