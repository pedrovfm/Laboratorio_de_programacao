vendas = {'Pedro':50, 'Maria':30, 'Jose':60, 'Carlos':38}
def gerarRelatorio(vendas):
  total = 0
  maiorVenda = 0    
  for k, v in vendas.items():
    total += v
    if v > maiorVenda:
      maiorVenda = v
    with open('/content/drive/MyDrive/arquivo.txt', 'a') as arquivo:
      arquivo.write(f'{k}: {v}\n')
  with open('/content/drive/MyDrive/arquivo.txt', 'a') as arquivo:
    arquivo.write(f'\nTotal de vendas: {total}\nMaior venda: {maiorVenda}')
  return arquivo
gerarRelatorio(vendas)